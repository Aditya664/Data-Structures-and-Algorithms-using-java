package Exercise;

public class print_one_five {
    public static void main(String[] args)
    {
        //for loop
        //Q. Print numbers from 1 to 5

        for(int num = 1;num <= 5; num++)
        {
            System.out.println(num);
        }

        //while loop
        int num = 1;
        while(num <= 5)
        {
            System.out.println(num);
            num++;
        }

    }
}
